//Write a program using Math Library function
#include<stdio.h>
#include<math.h>
int main()
{
    printf("%f\n",floor(19.9)); //floor
    printf("%f\n",round(75.6)); //round
    printf("%f\n",ceil(90)); //ceil
    printf("%f\n",sin(90)); //sin
    printf("%f\n",sinh(90)); //hyperbolic sin
    printf("%f\n",cos(90)); //cos
    printf("%f\n",cosh(90)); //hyperbolic cos
    printf("%f\n",tan(90)); //tan
    printf("%f\n",tanh(90)); //hyperbolic tan
    printf("%f\n",exp(2)); //exponential
    printf("%f\n",log(90)); //log
    printf("%f\n",log10(90)); //log of 10
    printf("%f\n",sqrt(3)); //square root
    printf("%f\n",pow(1,2)); //power
    printf("%f\n",trunc(180)); //truncates

}